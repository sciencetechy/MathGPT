def generate_numberlist(integer_size, num_of_oper, decimal_size, difficulty):
  integer_size = int(integer_size)
  num_of_oper = int(num_of_oper)
  decimal_size = int(decimal_size)
  numlist = []

  if difficulty == "Very Easy":
    if integer_size >= 3:
      integer_size -= 2
    elif integer_size == 2:
      integer_size -= 1

    if decimal_size > 0:
      if decimal_size >= 3:
        decimal_size -= 2
      elif decimal_size == 2:
        decimal_size -= 1

  elif difficulty == "Easy":
    if integer_size >= 2:
      integer_size -= 1

    if decimal_size > 0:
      if decimal_size >= 2:
        decimal_size -= 1

  elif difficulty == "Hard":
    integer_size += 1

    if decimal_size > 0:
      decimal_size += 1

  elif difficulty == "Very Hard":
    integer_size += 2

    if decimal_size > 0:
      decimal_size += 2

  if decimal_size == 0:
    for i in range(num_of_oper):
      random_num = random.randint(10**(integer_size-1),10**(integer_size)-1)
      numlist.append(random_num)

  else:
      for i in range(num_of_oper):
        random_num = random.randint(10**(integer_size-1),10**(integer_size)-1)
        random_num = random_num/10**decimal_size
        numlist.append(random_num)

  return numlist

